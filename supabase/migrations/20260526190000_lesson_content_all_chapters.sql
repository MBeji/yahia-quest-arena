## ⚡ البوابة الثانية: قواعد العمليات الأربع

### 🗡️ سلاح الجمع:
- **التبديل**: a + b = b + a
- **العنصر المحايد**: a + 0 = a
- **المعكوس**: a + (-a) = 0
- **التجميع**: a + (b + c) = (a + b) + c

### 🛡️ سلاح الضرب:
- **التبديل**: a × b = b × a
- **العنصر المحايد**: a × 1 = a
- **المقلوب**: a × (1/a) = 1 (حيث a ≠ 0)
- **التوزيع**: a(b + c) = ab + ac

### 🎯 قواعد الإشارات:
- (+) × (+) = (+) ⭐
- (-) × (-) = (+) ⭐
- (+) × (-) = (-) 💀
- a × b = 0 ⟹ a = 0 أو b = 0

