## ⚡ البوابة الثانية: مبرهنة طالس (Thalès)

> 💀 *"طالس هو سيد التناسب في المثلثات!"*

### 📖 إذا كان (MN) // (BC) في المثلث ABC:
$$\\frac{AM}{AB} = \\frac{AN}{AC} = \\frac{MN}{BC}$$

### 🎯 الاستعمالات:
1. **حساب أطوال** باستعمال التناسب
2. **إثبات التوازي**: إذا تساوت النسب

### مثال:
في مثلث ABC حيث M∈[AB] و N∈[AC] و (MN)//(BC):
AM = 3, AB = 9, AN = 2 ⟹ AC = ?
3/9 = 2/AC ⟹ AC = 6 ✅

