## 🛡️ البوابة الرابعة: الحصر (Encadrement)

> 💡 *"الحصر يحبس العدد بين حدين!"*

### 📖 التعريف:
a ≤ x ≤ b يعني أن x محصور بين a و b

### 🗡️ قواعد الحصر:
إذا a ≤ x ≤ b و c ≤ y ≤ d:
- ⚡ **الجمع**: a + c ≤ x + y ≤ b + d
- ⚡ **الطرح**: a - d ≤ x - y ≤ b - c (انتبه! نعكس y)
- ⚡ **الضرب بثابت موجب k**: ka ≤ kx ≤ kb
- ⚡ **المربع** (إذا x ≥ 0): a² ≤ x² ≤ b²
- ⚡ **المقلوب** (إذا 0 < a): 1/b ≤ 1/x ≤ 1/a

### 🎯 تطبيق:
إذا 2 ≤ x ≤ 5 و 1 ≤ y ≤ 3:
- ⚡ x + y: 3 ≤ x+y ≤ 8
- ⚡ x - y: 2-3 ≤ x-y ≤ 5-1 أي -1 ≤ x-y ≤ 4
- ⚡ 2x: 4 ≤ 2x ≤ 10

