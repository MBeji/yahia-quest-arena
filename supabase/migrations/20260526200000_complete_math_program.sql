## ⚡ البوابة الثانية: عكس نظرية فيثاغورس

> 💡 *"إذا تحققت العلاقة، فالمثلث قائم!"*

### 📖 النص:
في مثلث ABC، إذا كان BC² = AB² + AC² فإن المثلث **قائم في A**

### 🛡️ ملاحظة هامة:
إذا كان BC² ≠ AB² + AC²:
- ⚡ BC² > AB² + AC² ← المثلث **منفرج** في A
- ⚡ BC² < AB² + AC² ← المثلث **حاد** الزوايا

### 🎯 مثال:
مثلث أضلاعه 7, 8, 11:
- ⚡ 11² = 121
- ⚡ 7² + 8² = 49 + 64 = 113
- ⚡ 121 > 113 ← المثلث منفرج

