## 🔮 البوابة الثالثة: العلاقات القياسية في المثلث القائم

> 💡 *"الارتفاع النازل من القائمة يكشف علاقات سحرية!"*

### 📖 في مثلث ABC قائم في A، و H مسقط A على [BC]:

$$AH² = BH × HC$$
$$AB² = BH × BC$$
$$AC² = CH × CB$$

### 🗡️ كيف نستعمل هذه العلاقات:
- ⚡ لحساب ارتفاع المثلث القائم
- ⚡ لحساب مسقط ضلع على الوتر
- ⚡ لإثبات علاقات بين الأطوال

### 🎯 مثال:
مثلث ABC قائم في A، AB = 6, AC = 8:
- ⚡ BC = √(36 + 64) = 10
- ⚡ BH = AB²/BC = 36/10 = 3.6
- ⚡ HC = AC²/BC = 64/10 = 6.4
- ⚡ AH = √(BH × HC) = √(3.6 × 6.4) = √23.04 = **4.8**

