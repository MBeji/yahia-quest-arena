## 💀 البوابة الخامسة: المجالات (Intervalles)

### 📖 أنواع المجالات:

| الرمز | التسمية | المعنى |
|---|---|---|
| ⚡ [a, b] | مجال مغلق | a ≤ x ≤ b |
| ⚡ ]a, b[ | مجال مفتوح | a < x < b |
| ⚡ [a, b[ | نصف مفتوح | a ≤ x < b |
| ⚡ ]a, b] | نصف مفتوح | a < x ≤ b |
| ⚡ [a, +∞[ | غير محدود | x ≥ a |
| ⚡ ]-∞, b] | غير محدود | x ≤ b |

### 🎯 المجالات والقيمة المطلقة:
- ⚡ |x| ≤ a ⟺ -a ≤ x ≤ a ⟺ x ∈ [-a, a]
- ⚡ |x| ≥ a ⟺ x ≤ -a أو x ≥ a ⟺ x ∈ ]-∞, -a] ∪ [a, +∞[
- ⚡ |x - c| ≤ r ⟺ c - r ≤ x ≤ c + r

