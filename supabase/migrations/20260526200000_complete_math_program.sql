## 🏰 البوابة الأولى: مبرهنة طالس في المثلث

> 💡 *"طالس اكتشف أن المستقيمات المتوازية تقطع المتقاطعة بنسب متساوية!"*

### 📖 النص:
في مثلث ABC، إذا كانت (MN) // (BC) حيث M ∈ [AB] و N ∈ [AC]:

$$AM/AB = AN/AC = MN/BC$$

### 🗡️ التطبيق العكسي:
إذا كانت AM/AB = AN/AC (مع M ∈ [AB] و N ∈ [AC]) فإن **(MN) // (BC)**

### 🎯 مثال:
في مثلث ABC: AM = 3, AB = 9, AN = 2, AC = 6
- ⚡ AM/AB = 3/9 = 1/3
- ⚡ AN/AC = 2/6 = 1/3
- ⚡ بما أن AM/AB = AN/AC فإن (MN) // (BC)

