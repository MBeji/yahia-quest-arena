### 📖 القاعدة الذهبية:
إذا 0 ≤ a ≤ b فإن **√a ≤ √b**

### 🎯 أمثلة:
- ⚡ مقارنة √7 و √11: بما أن 7 < 11 فإن √7 < √11
- ⚡ مقارنة 3√2 و 2√3: (3√2)² = 18 و (2√3)² = 12 ← 3√2 > 2√3

