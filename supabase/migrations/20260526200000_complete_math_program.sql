## ⚡ البوابة الثانية: القاسم المشترك الأكبر (PGCD)

> 💡 *"القاسم المشترك الأكبر هو أقوى رابط بين عددين!"*

### 📖 التعريف:
PGCD(a, b) هو أكبر عدد يقسم a و b معاً

### 🗡️ طريقة الحساب (خوارزمية إقليدس):
- ⚡ نقسم الأكبر على الأصغر
- ⚡ ثم نقسم المقسوم عليه على الباقي
- ⚡ نواصل حتى يكون الباقي = 0
- ⚡ آخر مقسوم عليه هو PGCD

### 🎯 مثال: PGCD(48, 18)
- ⚡ 48 = 18 × 2 + 12
- ⚡ 18 = 12 × 1 + 6
- ⚡ 12 = 6 × 2 + **0**
- ⚡ إذن: PGCD(48, 18) = **6**

---

## 🔮 البوابة الثالثة: المضاعف المشترك الأصغر (PPCM)

### 📖 الصيغة السحرية:
$$PPCM(a, b) = (a × b) / PGCD(a, b)$$

### 🎯 مثال:
- ⚡ PPCM(48, 18) = (48 × 18) / 6 = 864 / 6 = **144**

