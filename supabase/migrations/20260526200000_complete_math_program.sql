إذا قطع مستقيم يمر من M ∈ [AD] موازٍ لـ (AB) الضلع [BC] في N:

$$DM/DA = DN/DC$$

### 🎯 تطبيق:
- ⚡ لإيجاد طول مجهول نستعمل تناسب النسب
- ⚡ مثال: DM = 4, DA = 10, DC = 15 ← DN = (4 × 15)/10 = 6

