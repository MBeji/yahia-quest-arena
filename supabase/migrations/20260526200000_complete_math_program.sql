## ⚡ البوابة الثانية: خاصيات الترتيب

### 🗡️ الترتيب والجمع:
- ⚡ إذا a ≤ b فإن a + c ≤ b + c (لكل عدد c)
- ⚡ إذا a ≤ b و c ≤ d فإن a + c ≤ b + d

### 🛡️ الترتيب والضرب:
- ⚡ إذا a ≤ b و **c > 0** فإن ac ≤ bc (الضرب بموجب يحافظ)
- ⚡ إذا a ≤ b و **c < 0** فإن ac ≥ bc (الضرب بسالب يعكس!)

### 🔥 الترتيب والمربع:
- ⚡ إذا 0 ≤ a ≤ b فإن a² ≤ b² (للموجبين)
- ⚡ إذا a ≤ b ≤ 0 فإن a² ≥ b² (للسالبين: العكس!)

### 💎 الترتيب والمقلوب:
- ⚡ إذا 0 < a ≤ b فإن 1/a ≥ 1/b
- ⚡ إذا a ≤ b < 0 فإن 1/a ≥ 1/b

